<h2 class="tituloS1">El exilio español en México:<br>80 años de legado</h2>
<br>
<p align="justify">Este 2019 se conmemoran los 80 años del exilio español en México. A finales de los años 1930, miles de españoles
  huyeron su país por la violencia, la Guerra Civil y la dictadura de Francisco Franco.
  <br><br>
  Hoy, estos exiliados hacen un llamado a apoyar a los centroamericanos y venezolanos que actualmente huyen por la
    violencia y la situación política que impactan a sus países en busca de un mejor futuro</p>
